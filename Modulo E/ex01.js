// while estrutura de repetição com texto lógico no inicio

var c = 1
while (c <= 6) { // enquanto o c for menorou igual a 6, irá repetir o que está dentro do while
    console.log(`Passo ${c}`)
    c++
}

console.log('')

// while estrutura de repetição com texto lógico no final

var n = 1
do { // faça, com o do o código corre pelo menos uma vez
    console.log(`Passo ${n}`)
    n++
} while(n <= 6)