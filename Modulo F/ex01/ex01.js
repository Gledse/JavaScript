// Array ou em português Vector
let num = [8, 4, 7] 
num[3] = 2 // irá adicionar o valor 6 na posição 3 ou indice 3
num.push(6) // irá adicionar o valor 7 na posição/indice a seguir

num.sort() // irá colocar o vector em ordem

console.log(`Nosso vector é o ${num}`)
console.log(`O vector tem ${num.length} posições`) // num.length >>> é um atributo para saber qual é o comprimento(quantos indices tem) do meu Vector